var CUSTOMANIM=[];
CUSTOMANIM["Custom"]=CUSTOMANIM["custom"]=CUSTOMANIM["CUSTOM"]=CUSTOM;
window.onload=function(){
	ANIMATIONS["Custom"]=CUSTOMANIM["Custom"];
	$("animations").onchange=showText;
	$("size").onchange=changeSize;
	$("turbo").onchange=setSpeed;
	$("normal").onchange=setSpeed;
	$("slowmo").onchange=setSpeed;
	$("start").onclick=runDisplay;
	$("stop").onclick=stopDisplay;
	halt();
};
function showText(){
	frameNum=0;
	var textArea=$("display");
	textArea.value=ANIMATIONS[this.value];
}
function changeSize(){
	var textArea=$("display");
	var choice=this.value;
	if(choice=="Custom"){SIZES["Custom"]=getCustomSize();}
	textArea.style.fontSize=SIZES[choice];
}
function setSpeed(){
	if($("turbo").checked){interval=SPEEDS["turbo"];}
	else if($("normal").checked){interval=SPEEDS["normal"];}
	else{interval=SPEEDS["slowmo"];}
	if(timer!==null){start();}
}
function getCustomSize(){
	var customSize=prompt("Enter font size: (e.g. 12pt)")||"12pt";
	return customSize;
}
function start(){
	clearInterval(timer);
	timer=setInterval(showFrame,interval);
	$("stop").disabled=false;
	$("start").disabled=true;
	$("animations").disabled=true;
}
function halt(){
	$("stop").disabled=true;
	$("start").disabled=false;
	$("animations").disabled=false;
}
function runDisplay(){
	frameArray=[];
	setSpeed();
	var fullAnim=$("display").value;
	var frames=fullAnim.split("=====\n");
	var i=0;
	while(frames.length>0){
		frameArray[i]=frames.shift();
		i++;
	}
	start();
}
function stopDisplay(){
	frameNum=0;
	clearInterval(timer);
	var toDisplay=ANIMATIONS[$("animations").value];
	$("display").value=toDisplay;
	halt();
}
function showFrame(){
	var textArea=$("display");
	if(frameNum===null){frameNum=0;}
	textArea.value=frameArray[frameNum];
	if(!reverseChecked()){
		frameNum++;
		if(frameNum==frameArray.length){frameNum=0;}
	}
	else{
		frameNum--;
		if(frameNum<0){frameNum=frameArray.length-1;}
	}
}
function reverseChecked(){return $("reverse").checked;}